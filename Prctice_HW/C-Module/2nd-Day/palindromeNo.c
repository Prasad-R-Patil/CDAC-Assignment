#include<stdio.h>

int main()
{
    int iNo = 0;

    printf("\n==============================================================================\n");
    printf("Enter The No :- ");
    scanf("%d",&iNo);
    printf("\n==============================================================================\n");

    int temp = iNo;
    int rev = 0;

    while (iNo != 0)
    {
        int rem = iNo % 10;

        rev = rev * 10 + rem;

        iNo = iNo / 10;

    }

    if(rev == temp)
    {
        printf("\n==============================================================================\n");
        printf("%d => This is palindrome No",rev);
        printf("\n==============================================================================\n");
    }
    else
    {
        printf("\n==============================================================================\n");
        printf("%d => This is Not palindrome No",rev);
        printf("\n==============================================================================\n");
    }
}